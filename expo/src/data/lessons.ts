function generateFENVariations(baseFENs: string[]): string[] {
  const variations: string[] = [];
  for (let i = 0; i < 10; i++) {
    variations.push(baseFENs[i % baseFENs.length]);
  }
  return variations;
}

function generateMoveVariations(baseMoves: string[][]): string[][] {
  const variations: string[][] = [];
  for (let i = 0; i < 10; i++) {
    variations.push(baseMoves[i % baseMoves.length]);
  }
  return variations;
}

