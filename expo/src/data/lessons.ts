function shuffleArray<T>(arr: T[]): T[] {
  const shuffled = [...arr];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
}

function generateLessons(): ChessLesson[] {
  const lessons: ChessLesson[] = [];
  let globalLessonNum = 0;

  for (const unit of UNIT_DEFINITIONS) {
    const titles = LESSON_TITLES[unit.category] || LESSON_TITLES.basics;
    const fenVariations = generateFENVariations(unit.fenTemplates);
    const moveVariations = generateMoveVariations(unit.movePatterns);

    for (let i = 0; i < unit.lessonCount; i++) {
      globalLessonNum++;
      const titleIndex = i % titles.length;
      const fenIndex = i % fenVariations.length;
      const moveIndex = i % moveVariations.length;

      const title = titles[titleIndex];
      const fen = fenVariations[fenIndex];
      const moves = moveVariations[moveIndex];

      const difficulty =
        unit.id <= 7
          ? "beginner"
          : unit.id <= 15
            ? "intermediate"
            : unit.id <= 21
              ? "advanced"
              : "master";

      const isBonus = i >= unit.lessonCount - 5;

      const hints = [
        `Look at the ${unit.category} pattern on the board`,
        `Consider the ${unit.title} techniques you've learned`,
        moves.length > 0
          ? `The solution involves moving to a key square`
          : `Think about piece coordination`,
      ];

      const explanations = [
        `In the ${unit.title}, it's important to understand ${title.toLowerCase()}. This move demonstrates the key principle.`,
        `${title} is a fundamental concept in chess ${unit.category}. Mastering this will improve your overall game.`,
        `Great work! The ${unit.title} is all about ${unit.description.toLowerCase()}. Practice this pattern to build intuition.`,
      ];

      const lessons_per_unit = unit.lessonCount;
      const lesson_within_unit = i + 1;

      lessons.push({
        id: `unit${unit.id}_lesson${i + 1}`,
        unitId: unit.id,
        lessonNumber: lesson_within_unit,
        title: `${title} (${lesson_within_unit}/${lessons_per_unit})`,
        description: `Master ${unit.title}: ${title.toLowerCase()}`,
        category: unit.category,
        difficulty: difficulty as ChessLesson["difficulty"],
        puzzleType: unit.puzzleType,
        fen,
        solutionMoves: moves,
        hints,
        explanation: explanations[i % explanations.length],
        xpReward: isBonus
          ? 30
          : difficulty === "beginner"
            ? 15
            : difficulty === "intermediate"
              ? 20
              : difficulty === "advanced"
                ? 25
                : 35,
        gemReward: isBonus ? 5 : 2,
        isBonus,
      });
    }
  }

  return lessons;
}

let cachedLessons: ChessLesson[] | null = null;
let cachedUnits: SkillUnit[] | null = null;

export function getAllLessons(): ChessLesson[] {
  if (!cachedLessons) {
    cachedLessons = generateLessons();
  }
  return cachedLessons;
}

export function getUnits(): SkillUnit[] {
  if (!cachedUnits) {
    cachedUnits = UNIT_DEFINITIONS.map((u) => ({
      id: u.id,
      title: u.title,
      description: u.description,
      category: u.category,
      icon: u.icon,
      color: u.color,
      lessonCount: u.lessonCount,
      status: "locked" as const,
    }));
    // Set first unit as active
    if (cachedUnits.length > 0) {
      cachedUnits[0].status = "active";
    }
  }
  return cachedUnits;
}

export function getLessonsByUnit(unitId: number): ChessLesson[] {
  return getAllLessons().filter((l) => l.unitId === unitId);
}

export function getLessonById(id: string): ChessLesson | undefined {
  return getAllLessons().find((l) => l.id === id);
}

export function getTotalLessonCount(): number {
  return getAllLessons().length;
}

export function getNextLesson(
  currentUnitId: number,
  currentLessonIndex: number,
): ChessLesson | undefined {
  const allLessons = getAllLessons();
  if (currentLessonIndex < allLessons.length) {
    return allLessons[currentLessonIndex];
  }
  return undefined;
}
